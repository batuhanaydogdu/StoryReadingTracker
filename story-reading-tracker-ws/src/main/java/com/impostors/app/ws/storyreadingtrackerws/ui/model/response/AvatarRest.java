package com.impostors.app.ws.storyreadingtrackerws.ui.model.response;

public class AvatarRest {

    private String avatarName;
    private String avatarId;
    private String avatarPrice;
    private String avatarURL;

    public String getAvatarName() {
        return avatarName;
    }

    public void setAvatarName(String avatarName) {
        this.avatarName = avatarName;
    }

    public String getAvatarId() {
        return avatarId;
    }

    public void setAvatarId(String avatarId) {
        this.avatarId = avatarId;
    }

    public String getAvatarPrice() {
        return avatarPrice;
    }

    public void setAvatarPrice(String avatarPrice) {
        this.avatarPrice = avatarPrice;
    }



    public String getAvatarURL() {
        return avatarURL;
    }

    public void setAvatarURL(String avatarURL) {
        this.avatarURL = avatarURL;
    }
}
